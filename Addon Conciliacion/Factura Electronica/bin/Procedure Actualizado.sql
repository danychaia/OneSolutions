ALTER PROCEDURE [dbo].[BUSCAR_CUENTAS] (@TIPO_BUSQUEDA VARCHAR(3),@BUSQUEDA VARCHAR(60))
AS

IF @TIPO_BUSQUEDA = '*' 
BEGIN
  SELECT A.AcctCode,B.AcctName , 
  ISNULL(A.Segment_0,'') AS CODIGO 
  FROM OACT A 
  INNER JOIN DSC1 B ON A.AcctCode = B.GLAccount 
END
ELSE
SELECT A.AcctCode,B.AcctName, ISNULL(A.Segment_0,'') AS CODIGO
 FROM OACT  A 
 INNER JOIN DSC1 B ON A.AcctCode = B.GLAccount 
 WHERE A.Segment_0 LIKE '%'+@BUSQUEDA+'%'
